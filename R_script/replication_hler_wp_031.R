ggsave("figures/figure_01_hist_total_cognition.png", p_hist, width = 7, height = 5)

# Scatter: main predictor vs outcome
p_scatter <- ggplot(data, aes(x = retire, y = total_cognition)) +
  geom_point(alpha = 0.3) +
  geom_smooth(method = "lm", colour = "firebrick") +
  labs(title = "retire vs total_cognition", x = "retire", y = "total_cognition") +
