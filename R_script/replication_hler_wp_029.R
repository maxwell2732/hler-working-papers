# ======================================================================
# ---------------------------------------------------------------------------
# Load Canonical Model Specification
# ---------------------------------------------------------------------------
# model_spec.json is the single source of truth generated by the HLER pipeline.
# It captures the exact variables, fixed effects, and SE choices used in Python.
spec <- fromJSON("model_spec.json")

# ======================================================================
# ---------------------------------------------------------------------------
# Data Loading
# ---------------------------------------------------------------------------
# Data path is read from model_spec.json at runtime:
data <- read_csv(spec$data_source)

# ======================================================================
# ---------------------------------------------------------------------------
# Variable Construction
# ---------------------------------------------------------------------------
# Apply sample restrictions matching the Python analysis:
data <- data %>%
  filter(rural2 != "农村",
          age >= 60)


# ======================================================================
# ---------------------------------------------------------------------------
# Main Regression
# ---------------------------------------------------------------------------
# DDML estimation using R ddml package
# Treatment variable and nuisance controls from model_spec.json:
treatment_var <- spec$treatment_var
nuisance_vars <- c(spec$independent_variables, spec$controls)
nuisance_vars <- nuisance_vars[nuisance_vars != treatment_var]

# Keep only columns present in data
nuisance_vars <- nuisance_vars[nuisance_vars %in% names(data)]
analysis_cols <- c(spec$dependent_variable, treatment_var, nuisance_vars)
reg_data <- data[, analysis_cols, drop = FALSE]

# Encode categorical (character/factor) columns as numeric dummies
# For binary columns (e.g. "是"/"否"), convert to 0/1; for multi-level, use dummies
cat_cols <- names(reg_data)[sapply(reg_data, function(x) is.character(x) || is.factor(x))]
for (col in cat_cols) {
  vals <- unique(na.omit(reg_data[[col]]))
  if (length(vals) == 2) {
    reg_data[[col]] <- as.integer(reg_data[[col]] == vals[2])
  } else if (length(vals) > 2 && length(vals) <= 15) {
    # Create dummy columns manually (avoids model.matrix NA row-drop issue)
    for (v in vals[-1]) {
      dummy_name <- paste0(col, "_", v)
      reg_data[[dummy_name]] <- as.integer(reg_data[[col]] == v)
      reg_data[[dummy_name]][is.na(reg_data[[col]])] <- NA
      if (col %in% nuisance_vars) nuisance_vars <- c(nuisance_vars, dummy_name)
    }
    nuisance_vars <- nuisance_vars[nuisance_vars != col]
    reg_data[[col]] <- NULL
  } else {
    reg_data[[col]] <- as.numeric(reg_data[[col]])
  }
}

reg_data <- na.omit(reg_data)
# Ensure all columns are numeric
reg_data <- as.data.frame(lapply(reg_data, as.numeric))

Y <- reg_data[[spec$dependent_variable]]
D <- reg_data[[treatment_var]]
X <- as.matrix(reg_data[, nuisance_vars, drop = FALSE])

ddml_model_type <- spec$ddml_model  # PLR or IRM
ddml_learner    <- spec$ddml_learner # lasso, random_forest, gradient_boosting
ddml_n_folds    <- spec$ddml_n_folds

# Select learner — ddml uses mdl_* wrapper functions from the ddml package
if (ddml_learner == "random_forest") {
  learners <- list(list(fun = mdl_ranger, args = list(num.trees = 100, max.depth = 5)))
} else if (ddml_learner == "gradient_boosting") {
  learners <- list(list(fun = mdl_xgboost, args = list(nrounds = 100, max_depth = 3, verbose = 0)))
} else {
  # Default: lasso via glmnet
  learners <- list(list(fun = mdl_glmnet))
}

# Fit DDML model
if (ddml_model_type == "IRM") {
  model_ddml <- ddml_ate(Y, D, X,
                        learners = learners,
                        sample_folds = ddml_n_folds,
                        silent = TRUE)
} else {
  model_ddml <- ddml_plr(Y, D, X,
                        learners = learners,
                        sample_folds = ddml_n_folds,
                        silent = TRUE)
}
summary(model_ddml)

