# Cross-sectional OLS on gender == "女性" subgroup:
model_het_女性 <- lm(oopdoc1m ~ ea001s1 + age + edu + retire + pension + chronic_index + wave + rural2,
                     data = data %>% filter(gender == "女性"))
print(coeftest(model_het_女性, vcov = vcovHC(model_het_女性, "HC3")))

# Cross-sectional OLS on gender == "男性" subgroup:
model_het_男性 <- lm(oopdoc1m ~ ea001s1 + age + edu + retire + pension + chronic_index + wave + rural2,
                     data = data %>% filter(gender == "男性"))
print(coeftest(model_het_男性, vcov = vcovHC(model_het_男性, "HC3")))

# --- by chronic disease burden (median split) ---
data <- data %>%
  mutate(chronic_index_q = ntile(chronic_index, 2))
for (g in 1:2) {
  sub  <- data %>% filter(chronic_index_q == g)
  m    <- lm(oopdoc1m ~ ea001s1 + age + edu + retire + pension + chronic_index + gender + wave + rural2, data = sub)
  cat("\n--- by chronic disease burden (median split), group", g, "---\n")
  print(coeftest(m, vcov = vcovHC(m, "HC3")))
}

# --- by retirement status ---
# Cross-sectional OLS on retire == "否" subgroup:
model_het_否 <- lm(oopdoc1m ~ ea001s1 + age + edu + pension + chronic_index + gender + wave + rural2,
                     data = data %>% filter(retire == "否"))
print(coeftest(model_het_否, vcov = vcovHC(model_het_否, "HC3")))

# Cross-sectional OLS on retire == "是" subgroup:
