# ======================================================================
# ---------------------------------------------------------------------------
# Package Loading
# ---------------------------------------------------------------------------
# Install missing packages:  install.packages(readLines('packages.txt'))

library(fixest)
library(ggplot2)
library(jsonlite)
library(lmtest)
library(modelsummary)
library(sandwich)
library(tidyverse)

# ======================================================================
# ---------------------------------------------------------------------------
# Load Canonical Model Specification
# ---------------------------------------------------------------------------
# model_spec.json is the single source of truth generated by the HLER pipeline.
# It captures the exact variables, fixed effects, and SE choices used in Python.
spec <- fromJSON("model_spec.json")

# ======================================================================
# ---------------------------------------------------------------------------
# Data Loading
# ---------------------------------------------------------------------------
# Data path is read from model_spec.json at runtime:
data <- read_csv(spec$data_source)

# ======================================================================
# ---------------------------------------------------------------------------
# Variable Construction
# ---------------------------------------------------------------------------
# Dep var was a Yes/No character column in Python — convert to 0/1 for LPM:
data <- data %>%
  mutate(DIED = as.integer(DIED == "Yes"))


# ======================================================================
# ---------------------------------------------------------------------------
# Main Regression
# ---------------------------------------------------------------------------
# Build fixed-effects formula from model_spec.json:
indep_vars <- c(spec$independent_variables, spec$controls)
rhs        <- paste(indep_vars, collapse = " + ")
# feols uses ^ to form a compound entity FE (e.g. hhid^line);
# paste with ^ when multiple FE columns, otherwise use single name:
fe_cols <- spec$fixed_effects
fe_part <- if (length(fe_cols) > 1) paste(fe_cols, collapse = "^") else fe_cols[1]
formula_fe <- as.formula(paste(spec$dependent_variable, "~", rhs, "|", fe_part))

# Fixed-effects estimator with robust SEs (type from spec):
model_fe <- feols(formula_fe, data = data, vcov = spec$se_type)
summary(model_fe)

# ======================================================================
# ---------------------------------------------------------------------------
# Heterogeneity Analysis
# ---------------------------------------------------------------------------
# --- by birth order (early vs. late) ---
data <- data %>%
  mutate(BIRTH_ORDER_q = ntile(BIRTH_ORDER, 2))
for (g in 1:2) {
  sub  <- data %>% filter(BIRTH_ORDER_q == g)
  m    <- lm(DIED ~ RANK + AGE_IN_SUI + BIRTH_ORDER + GENERATION + BANNER + BIRTHYEAR + BIRTH_ORDER_SEX, data = sub)
  cat("\n--- by birth order (early vs. late), group", g, "---\n")
  print(coeftest(m, vcov = vcovHC(m, "HC3")))
}

# --- by generation within lineage ---
data <- data %>%
  mutate(GENERATION_q = ntile(GENERATION, 4))
for (g in 1:4) {
  sub  <- data %>% filter(GENERATION_q == g)
  m    <- lm(DIED ~ RANK + AGE_IN_SUI + BIRTH_ORDER + GENERATION + BANNER + BIRTHYEAR + BIRTH_ORDER_SEX, data = sub)
  cat("\n--- by generation within lineage, group", g, "---\n")
  print(coeftest(m, vcov = vcovHC(m, "HC3")))
}

# --- by banner affiliation ---
# Cross-sectional OLS on BANNER == 0 subgroup:
model_het_0 <- lm(DIED ~ RANK + AGE_IN_SUI + BIRTH_ORDER + GENERATION + BANNER + BIRTHYEAR + BIRTH_ORDER_SEX,
                     data = data %>% filter(BANNER == 0))
print(coeftest(model_het_0, vcov = vcovHC(model_het_0, "HC3")))

# Cross-sectional OLS on BANNER == 1 subgroup:
model_het_1 <- lm(DIED ~ RANK + AGE_IN_SUI + BIRTH_ORDER + GENERATION + BANNER + BIRTHYEAR + BIRTH_ORDER_SEX,
                     data = data %>% filter(BANNER == 1))
print(coeftest(model_het_1, vcov = vcovHC(model_het_1, "HC3")))


# ======================================================================
# ---------------------------------------------------------------------------
# Temporal Robustness Check
# ---------------------------------------------------------------------------
# Split at year 1800 (pre / post)
data_pre  <- data %>% filter(YEAR <  1800)
data_post <- data %>% filter(YEAR >= 1800)

model_pre  <- lm(DIED ~ RANK + AGE_IN_SUI + BIRTH_ORDER + GENERATION + BANNER + BIRTHYEAR + BIRTH_ORDER_SEX, data = data_pre)
model_post <- lm(DIED ~ RANK + AGE_IN_SUI + BIRTH_ORDER + GENERATION + BANNER + BIRTHYEAR + BIRTH_ORDER_SEX, data = data_post)

cat("\n--- Pre-1800 ---\n")
print(coeftest(model_pre,  vcov = vcovHC(model_pre,  "HC3")))
cat("\n--- Post-1800 ---\n")
print(coeftest(model_post, vcov = vcovHC(model_post, "HC3")))

