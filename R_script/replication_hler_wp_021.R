# Model:      FE_within (Person-level panel OLS with individual fixed effects; regress survival indicator (1 - DIED) on father's RANK interacted with AGE_IN_SUI quintiles; lineage (FOUNDER_ID) fixed effects control for persistent clan-level health environments; cohort fixed effects from BIRTHYEAR decade)
