ggsave("figures/figure_01_hist_doctor.png", p_hist, width = 7, height = 5)

# Scatter: main predictor vs outcome
p_scatter <- ggplot(data, aes(x = ea001s3, y = doctor)) +
  geom_point(alpha = 0.3) +
  geom_smooth(method = "lm", colour = "firebrick") +
  labs(title = "ea001s3 vs doctor", x = "ea001s3", y = "doctor") +
