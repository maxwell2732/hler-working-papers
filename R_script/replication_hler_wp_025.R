# ======================================================================
# ---------------------------------------------------------------------------
# Package Loading
# ---------------------------------------------------------------------------
# Install missing packages:  install.packages(readLines('packages.txt'))

library(fixest)
library(ggplot2)
library(jsonlite)
library(lmtest)
library(modelsummary)
library(sandwich)
library(tidyverse)

# ======================================================================
# ---------------------------------------------------------------------------
# Load Canonical Model Specification
# ---------------------------------------------------------------------------
# model_spec.json is the single source of truth generated by the HLER pipeline.
# It captures the exact variables, fixed effects, and SE choices used in Python.
spec <- fromJSON("model_spec.json")

# ======================================================================
# ---------------------------------------------------------------------------
# Data Loading
# ---------------------------------------------------------------------------
# Data path is read from model_spec.json at runtime:
data <- read_csv(spec$data_source)

# ======================================================================
# ---------------------------------------------------------------------------
# Variable Construction
# ---------------------------------------------------------------------------

# ======================================================================
# ---------------------------------------------------------------------------
# Main Regression
# ---------------------------------------------------------------------------
# Build fixed-effects formula from model_spec.json:
indep_vars <- c(spec$independent_variables, spec$controls)
rhs        <- paste(indep_vars, collapse = " + ")
# feols uses ^ to form a compound entity FE (e.g. hhid^line);
# paste with ^ when multiple FE columns, otherwise use single name:
fe_cols <- spec$fixed_effects
fe_part <- if (length(fe_cols) > 1) paste(fe_cols, collapse = "^") else fe_cols[1]
formula_fe <- as.formula(paste(spec$dependent_variable, "~", rhs, "|", fe_part))

# Fixed-effects estimator with robust SEs (type from spec):
model_fe <- feols(formula_fe, data = data, vcov = spec$se_type)
summary(model_fe)

# ======================================================================
# ---------------------------------------------------------------------------
# Heterogeneity Analysis
# ---------------------------------------------------------------------------
# --- by sex ---
# Cross-sectional OLS on gender == 1 subgroup:
model_het_1 <- lm(indinc_cpi ~ animal_protein_vol + age + gender + b4 + t2,
                     data = data %>% filter(gender == 1))
print(coeftest(model_het_1, vcov = vcovHC(model_het_1, "HC3")))

# Cross-sectional OLS on gender == 2 subgroup:
model_het_2 <- lm(indinc_cpi ~ animal_protein_vol + age + gender + b4 + t2,
                     data = data %>% filter(gender == 2))
print(coeftest(model_het_2, vcov = vcovHC(model_het_2, "HC3")))

# --- by urban/rural residence ---
# Cross-sectional OLS on t2 == 1 subgroup:
model_het_1 <- lm(indinc_cpi ~ animal_protein_vol + age + gender + b4 + t2,
                     data = data %>% filter(t2 == 1))
print(coeftest(model_het_1, vcov = vcovHC(model_het_1, "HC3")))

# Cross-sectional OLS on t2 == 2 subgroup:
model_het_2 <- lm(indinc_cpi ~ animal_protein_vol + age + gender + b4 + t2,
                     data = data %>% filter(t2 == 2))
print(coeftest(model_het_2, vcov = vcovHC(model_het_2, "HC3")))

# --- by baseline income level ---
data <- data %>%
  mutate(indinc_cpi_q = ntile(indinc_cpi, 2))
for (g in 1:2) {
  sub  <- data %>% filter(indinc_cpi_q == g)
  m    <- lm(indinc_cpi ~ animal_protein_vol + age + gender + b4 + t2, data = sub)
  cat("\n--- by baseline income level, group", g, "---\n")
  print(coeftest(m, vcov = vcovHC(m, "HC3")))
}


# ======================================================================
# ---------------------------------------------------------------------------
# Temporal Robustness Check
# ---------------------------------------------------------------------------
# Split at year 2000 (pre / post)
data_pre  <- data %>% filter(wave <  2000)
data_post <- data %>% filter(wave >= 2000)

model_pre  <- lm(indinc_cpi ~ animal_protein_vol + age + gender + b4 + t2, data = data_pre)
model_post <- lm(indinc_cpi ~ animal_protein_vol + age + gender + b4 + t2, data = data_post)

cat("\n--- Pre-2000 ---\n")
print(coeftest(model_pre,  vcov = vcovHC(model_pre,  "HC3")))
cat("\n--- Post-2000 ---\n")
print(coeftest(model_post, vcov = vcovHC(model_post, "HC3")))

